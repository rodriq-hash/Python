# python tuples 
counties = ("Embu", "Kajiado", "Nairobi", "Nakuru", "Bomet", "Mombasa")
# show output 
print(counties)
#  print county at index 3
print(counties[3])

# slicing 
# 1. print counties from index 2 and above  
print(counties[2:])

# 2. print from index 2 upto index 5 
print(counties[2:5]) 
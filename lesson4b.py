# python for loops 
# loop from 1 to 10
for index in range (1,10,1) :
    print(index)
# loop from 20 to 100 
for count in range (50,100,1) :
    print(count)
# loop all even numbers from 0 to 100 
for even in range (0,100,2) :
    print(even)
# loop all years election was taken place in kenya
for years in range (1997,2023,5) :
    print(years)